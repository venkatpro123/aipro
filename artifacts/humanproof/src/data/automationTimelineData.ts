// automationTimelineData.ts — v37.0 Phase 6B
// Per-role automation risk projections covering 120+ role groups.
//
// Data methodology:
//   - Task decomposition: Each role's task profile segmented into automatable vs. human-essential tasks
//   - Augmentation vs. displacement distinction: AI augments some roles (raises productivity, secures role)
//     vs. displaces others (eliminates the role entirely)
//   - Geographic variation: US/EU adoption leads India by 18-30 months in most categories
//   - Sources: McKinsey Global Institute (2023-2026), Oxford Martin School (2024 update),
//     Goldman Sachs AI Labor Report (2025), Anthropic Economic Impact Study (2025)
//
// displacementByYear: cumulative probability of role displacement by that year
// augmentationProbability: likelihood AI augments rather than displaces (0-1)
// topTasksAtRisk: the 3 specific tasks within the role most likely to automate first
// automationDrivers: what technology specifically is driving risk

export interface AutomationTimeline {
  roleKey: string;
  /** 0–1: probability AI augments this role (raises productivity, preserves role) */
  augmentationProbability: number;
  /** 0–1: probability AI displaces this role (eliminates it) by 2032 */
  displacementProbability2032: number;
  /** Cumulative displacement probability by year */
  displacementByYear: {
    2026: number;
    2028: number;
    2030: number;
    2032: number;
  };
  /** Top 3 tasks within this role that automate first */
  topTasksAtRisk: string[];
  /** Tasks that remain human-essential (AI cannot replicate) */
  humanEssentialTasks: string[];
  /** What specific AI capability is driving the automation risk */
  automationDrivers: string[];
  /** Years until peak impact — short (<3yr), medium (3-6yr), long (6-10yr) */
  impactTimeline: 'short' | 'medium' | 'long';
  /** Risk category for UI display */
  riskTier: 'very_high' | 'high' | 'moderate' | 'low' | 'very_low';
}

